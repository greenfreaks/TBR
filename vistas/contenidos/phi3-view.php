    <h6 class="txt-dark-blue bold">1. ¿En qué consiste el PHI ³?</h6>
      <p class="justify"><b class="txt-blue-tbr">El Programa de Habilidades en Investigación e Innovación en la Industria</b>, consiste en cursos diseñados para que estudiantes, posgraduados e investigadores en la academia, tengan acercamientos con la industria que amplíen sus experiencias y faciliten la identificación de oportunidades de desarrollo profesional, contratos de servicios científicos y tecnológicos o alianzas para desarrollar o comercializar tecnologías.</p>

    <h6 class="txt-dark-blue bold">2. ¿Qué requisitos se necesitan para inscribirse?</h6>
    <ul>
      <li class="justify">Ser estudiante o egresado de posgrado, profesor o investigador en áreas de ciencia, tecnología o humanidades en alguna institución académica.</li>
      <li class="justify">Cubrir la cuota de inscripción, ya sea por módulo o bien por el programa completo. Si eliges el programa completo, se aplica un descuento del 20%.</li>
      <li class="justify">Tener interés en interactuar con la industria para detectar oportunidades de desarrollo profesional, ofertar servicios científicos o de colaboración.</li>
    </ul>

    <h6 class="txt-dark-blue bold">3. ¿Cómo está estructurado el PHI ³?</h6>
    <ul>
      <li class="justify">El programa está organizado en 4 módulos que hay que tomar en 4 meses (uno por mes).</li>
      <li class="justify">Cada módulo consta de 8 clases a tomar en tus tiempos disponibles y 2 clases en tiempo real, una al inicio y otra al cierre de cada módulo.</li>
      <li class="justify">Cada clase dura entre 1 y 2 horas y tienen una secuencia didáctica ya establecida para optimizar el aprendizaje.</li>
      <li class="justify">Las clases en horario abierto están pregrabadas, mientras que la clase al inicio y final de cada módulo es en tiempo real para fomentar la interacción con el instructor y los estudiantes.</li>
    </ul>

    <h6 class="txt-dark-blue bold">4. ¿Cuánto tiempo debo invertir en eL PHI ³?</h6>
    <p class="justify">Cada módulo requerirá de hasta 2 horas semanales durante un mes.</p>

    <h6 class="txt-dark-blue bold">5. ¿Puedo tomar módulos sueltos del PHI ³?</h6>
    <p class=" justify"><b class="txt-blue-tbr">Aunque esto es viable,</b> recomendamos que se tomen los cuatro módulos en el orden propuesto, para que su formación sea integral y puedan generar las evidencias de aprendizaje que se presentarán a empresas de base tecnológica.</p>

    <h6 class="txt-dark-blue bold">6. ¿Cuál es el resultado por cursar el PHI ³?</h6>
    <p><b class="txt-blue-tbr">Cada asistente desarrollará </b> una capeta de evidencias de aprendizaje, que se presentará a empresas de base tecnológica para crear oportunidades de colaboración, servicios, contratación o hasta una transferencia de tecnología.</p>

    <h6 class="txt-dark-blue bold">7. ¿Qué pasa una vez que termine de cursar el PHI ³?</h6>
    <p><b class="txt-blue-tbr">Al terminar de cursar el programa</b> recibirás una constancia de habilidades para inscribirte en nuestro repositorio de tecnologías y talentos en ciencia, tecnología y humanidades. Además, integraremos esta constancia a tu carpeta de evidencias de aprendizaje, la cual contendrá una propuesta de análisis, colaboración o desarrollo tecnológico para presentar a empresas innovadoras. Si la empresa considera viable la propuesta, te integrarás a un equipo de proyecto para trabajar en el desarrollo de soluciones a sus retos, con lo que ganarás experiencia en investigación, desarrollo e innovación.</p>

    <h6 class="txt-dark-blue bold">8. ¿Cómo se presentarán mis resultados del PHI ³ a empresas?</h6>
    <p><b class="txt-blue-tbr">Seleccionaremos a empresas </b> de nuestra base de datos para identificar a aquellas que, por su perfil y objetivos, son las más pertinentes para presentarles propuestas de colaboración, servicios o contratación.</p>

    <h6 class="txt-dark-blue bold">9. ¿Cómo se identificarán a las empresas para presentar mis evidencias de aprendizaje y tecnologías?</h6>
    <p><b class="txt-blue-tbr">Contamos con una base de datos de empresas innovadoras,</b> desde grandes empresas líderes de sectores industriales hasta startups, que cuentan con retos de innovación y necesidades de tecnologías y talentos.</p>

